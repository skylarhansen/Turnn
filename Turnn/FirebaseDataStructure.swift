/*  FIREBASE DATA STRUCTURE REFERENCE
 
 (all identifiers are Strings generated by Firebase.
  in some cases we'll need to retrieve formerly created
  identifers [like when we append an event identifer to a
  user's record, etc.])
 
 Events
     - identifier
         - title: String
         - location:
             - lat: Double
             - long: Double
             - address: String
             - city: String
             - state: String
             - zip: String
         - startTime: Double
         - endTime: Double
         - categories
             - identifier: true
             - identifier: true
         - description: String
         - passwordPro: Bool
         - password: String
         - contactInfo: String
         - host: identifier (there can only be one host,
                            given how our model is set up,
                            otherwise this spot would look
                            like location & categories)
 
 Users
     - identifier
     (email and password managed by Firebase directly)
         - username: String
         - hostname: String
         - paid: Bool
         - events
             - identifier: true
             - identifier: true
 
 Locations
     - identifier
        - g:  (geohash--String)
        - l
             - 0:  (latitude--Double)
             - 1:  (longitude--Double)
        - eventIdentifier: String
 
 
 
 
 */





